function getRandomGreet() {
  const greetNames = Object.keys(GREETS);
  const contents: string[][] = greetNames.map((k) => GREETS[k]);
  const randomGreet = contents[Math.floor(Math.random() * contents.length)];

  return randomGreet;
}

